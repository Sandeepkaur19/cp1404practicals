try:
    numerator = int(input("Enter the numerator: "))
    denominator = int(input("Enter the denominator: "))
    fraction = numerator / denominator
    print(fraction)
except ValueError:
    print("Numerator and denominator must be valid numbers!")
except ZeroDivisionError:
    print("Cannot divide by zero!")
print("Finished.")

# 1 The ValueError only occur if you try and input a non integer
# 2 The ZeroDivisionError occur if a zero is inputted as the denominator
# 3 Yes, it is possible using "var_list" or "try..except"


